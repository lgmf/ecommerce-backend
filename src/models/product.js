class Product {
  constructor(color, department, description, image, material, name, price, stock) {
    this.color = color;
    this.department = department;
    this.description = description;
    this.image = image;
    this.material = material;
    this.name = name;
    this.price = price;
    this.stock = stock;
  }
}

module.exports = Product;
